package objects_misc;

public class ReactionCount {
	long reaction, count;
	
	public ReactionCount() {
		
	}

	public long getReaction() {
		return reaction;
	}

	public void setReaction(long reaction) {
		this.reaction = reaction;
	}

	public long getCount() {
		return count;
	}

	public void setCount(long count) {
		this.count = count;
	}
}
